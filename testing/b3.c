/* final program for boolean values */
#include <stdio.h>

int main(void) {
    /* initialize variables */
    int i = 0, j = 1;
    /* k - variable for storing bool value */
    int k;

    /* set k variable equal to 1 and
     * print the result.
     */
    k = (i >= 0 && j <= 00) || (i <= 0 && j >= 00);
    printf("%d\n",k);
    return 0;
}
